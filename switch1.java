public class switch1 {

    public static void main(String[] args) {

        int i;
        i = 2;

        switch(i) {
            case 1:
              System.out.println("A");
            case 2:
              break;
            case 3:
              System.out.println("B");
            case 4:
            default:
              System.out.println("C");
        }
        }
    }